// Grade calculation by using switch case


function Weekdays(days){

switch (days){
    case "Monday": 
    console.log("Monday");
    break;
    case "Tuesday":
    console.log("Tuesday");
    break
    case "Wednesday":
    console.log("Wednesday");
    break
    default:
        console.log("Invalid day");    
        break;     
    
}

}
Weekdays("Wednesday");

